#include <stdio.h>

int main()
{
    printf("I am 20 years old.\n");
    printf("I am %d years old.\n", 20); // %d is a format for integers numbers.
    printf(" I am %d years old and my father is %d years old.\n", 20, 50);
    printf("My lucky number is %d\n", 7.3);
    printf("My lucky number is %f\n", 7.3); // %f is a format for floating-point numbers.

    return 0;
}