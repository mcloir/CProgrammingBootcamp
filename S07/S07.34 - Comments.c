#include <stdio.h>

int main()
{
    // This indicates a comment in C. It is ignored by the compiler and is used to explain the code to humans.
    printf("    *\n");
    printf("   ***\n");
    printf("  *****\n");
    printf(" *******\n");


    /* This is another way to write comments in C, but in this case, it is a multi-line comment that spans
    multiple lines */
    printf("   *\n  ***\n *****\n");
    return 0;
}